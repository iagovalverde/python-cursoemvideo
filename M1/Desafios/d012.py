# CALCULANDO DESCONTOS
# Faca um algoritmo que leia o preco de um produto e mostre seu novo preco, com 5% de desconto

preco_produto = float(input('Digite o valor do produto: '))
preco_desconto = preco_produto * 0.95
print(f'O produto com valor de {preco_produto} possui desconto de 5%, ficando com o valor de {preco_desconto}')