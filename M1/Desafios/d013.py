# REAJUSTE SALARIAL
# Faca um algoritmo que leia o salario de um funcionario e mostre seu novo salario, com 15% de aumento

salario = float(input('Digite o salario: '))
salario_aumento = salario * 1.15
print(f'O salario de {salario} obteve um aumento de 15% ficando com o valor de {salario_aumento}')